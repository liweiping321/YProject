package com.road.fire.entity.cfg;

import org.simpleframework.xml.Attribute;
import org.simpleframework.xml.Root;

@Root(name="server")
public class MatchServerCfg {

	@Attribute(name="ip")
	private String ip;
	
	@Attribute
	private int port;

	public String getIp() { 
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public int getPort() {
		return port;
	}

	public void setPort(int port) {
		this.port = port;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((ip == null) ? 0 : ip.hashCode());
		result = prime * result + port;
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		MatchServerCfg other = (MatchServerCfg) obj;
		if (ip == null) {
			if (other.ip != null)
				return false;
		} else if (!ip.equals(other.ip))
			return false;
		if (port != other.port)
			return false;
		return true;
	}
	
	
	
}
